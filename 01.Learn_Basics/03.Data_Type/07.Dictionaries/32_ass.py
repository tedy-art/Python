# Creating a dictionary
Dict = {}
print("Empty Dictionary : ")
print(Dict)

# creating dictionary using dict() method
D = dict({1:"hi", 2:"hello", 3:"Welcome"})
print("\ndict() method : ")
print(D)

Dict = dict({1: 'Geeks', 2: 'For', 3: 'Geeks'})
print("\nDictionary with the use of dict(): ")
print(Dict)

# Create a dictionary with each item as Pair
Dict = dict([(1 , 'hi'), (2 , 'Hello')])
print("\nDictionary with each item as pair : ")
print(Dict)