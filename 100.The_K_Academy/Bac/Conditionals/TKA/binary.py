# Decimal [range 0 to 9, base 10 system]
my_var = 222
print(type(my_var))
print(my_var)

# binary [range 0 to 1, base 2 system]
my_var = 0b10101
print(type(my_var))
print(my_var)

# Octal [range 0 to 7, base 8 system]
my_var = 0o342
print(type(my_var))
print(my_var)

# Hexadecimal [range 0 to 9 and A to F, base 16 system]
my_var = 0xFace23
print(type(my_var))
print(my_var)