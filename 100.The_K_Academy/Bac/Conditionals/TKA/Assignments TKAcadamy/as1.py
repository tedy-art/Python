# Converting decimal to binary
a = 23
print("Decimal : ",a)
x = bin(a)
print("convert decimal to binary : ",x)

# Converting decimal to octal
b = 34222
print('Decimal : ',b)
x = oct(b)
print("convert decimal to octal : ",x)

# Converting decimal to hexa
c = 2342
print("Decimal : ",c)
x = hex(c)
print("Convert decimal to hexa : ", c)
